# Create a list with 5 elements
my_list = [10, 20, 30, 40, 50]

# Access elements in the list by index
print(f"First element in the list: {my_list[0]}")
print(f"Third element in the list: {my_list[2]}")
print(f"Last element in the list: {my_list[-1]}")

# Create a tuple with 5 elements
my_tuple = ('apple', 'banana', 'cherry', 'date', 'elderberry')

# Access elements in the tuple by index
print(f"First element in the tuple: {my_tuple[0]}")
print(f"Third element in the tuple: {my_tuple[2]}")
print(f"Last element in the tuple: {my_tuple[-1]}")

# Create a dictionary with 5 key-value pairs
my_dict = {
    'name': 'Alice',
    'age': 25,
    'city': 'New York',
    'job': 'Engineer',
    'hobby': 'Reading'
}

# Access elements in the dictionary by key
print(f"Value for key 'name': {my_dict['name']}")
print(f"Value for key 'city': {my_dict['city']}")
print(f"Value for key 'hobby': {my_dict['hobby']}")